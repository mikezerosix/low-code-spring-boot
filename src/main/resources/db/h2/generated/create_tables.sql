CREATE TABLE author (
        created timestamp(6),
        id bigint not null,
        modified timestamp(6),
        publisher_id bigint,
        version bigint not null,
        creator varchar(255),
        firstname varchar(255),
        lastname varchar(255),
        modifier varchar(255),
        primary key (id)
    );
CREATE TABLE author_aud (
        rev integer not null,
        revtype tinyint,
        created timestamp(6),
        id bigint not null,
        modified timestamp(6),
        publisher_id bigint,
        creator varchar(255),
        firstname varchar(255),
        lastname varchar(255),
        modifier varchar(255),
        primary key (rev, id)
    );
CREATE TABLE book (
        author_id bigint,
        created timestamp(6),
        id bigint not null,
        modified timestamp(6),
        version bigint not null,
        isbn varchar(13) not null unique,
        creator varchar(255),
        modifier varchar(255),
        title varchar(255) not null,
        primary key (id)
    );
CREATE TABLE book_aud (
        rev integer not null,
        revtype tinyint,
        author_id bigint,
        created timestamp(6),
        id bigint not null,
        modified timestamp(6),
        creator varchar(255),
        isbn varchar(255),
        modifier varchar(255),
        title varchar(255),
        primary key (rev, id)
    );
CREATE TABLE publisher (
        created timestamp(6),
        id bigint not null,
        modified timestamp(6),
        version bigint not null,
        vat varchar(9) not null unique,
        creator varchar(255),
        modifier varchar(255),
        name varchar(255) not null,
        url varchar(255),
        primary key (id)
    );
CREATE TABLE publisher_aud (
        rev integer not null,
        revtype tinyint,
        created timestamp(6),
        id bigint not null,
        modified timestamp(6),
        creator varchar(255),
        modifier varchar(255),
        name varchar(255),
        url varchar(255),
        vat varchar(255),
        primary key (rev, id)
    );
CREATE TABLE revinfo (
        rev integer generated by default as identity,
        revtstmp bigint,
        primary key (rev)
    );
